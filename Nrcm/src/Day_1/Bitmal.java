package Day_1;

public class Bitmal {

	public static void main(String[] args) {
		int a=5;
		int b=2;
		System.out.println(a<<2);//5:00101<<2-after left shift it becomes 10100(16)
        System.out.println(a>>2);//5:00101<<2-after left shift it becomes 00001(1)
        System.out.println(a&b);
        System.out.println(a|b);
	}

}
